package com.yu.yubase.entities;

public class BanPage {
	String id;
	String adsName;
	String endTime;
	String linkUrl;
	String picUrl;
	String startTime;
	String orderId;
	String typeId;
	public final String getId() {
		return id;
	}
	public final void setId(String id) {
		this.id = id;
	}
	public final String getAdsName() {
		return adsName;
	}
	public final void setAdsName(String adsName) {
		this.adsName = adsName;
	}
	public final String getEndTime() {
		return endTime;
	}
	public final void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public final String getLinkUrl() {
		return linkUrl;
	}
	public final void setLinkUrl(String linkUrl) {
		this.linkUrl = linkUrl;
	}
	public final String getPicUrl() {
		return picUrl;
	}
	public final void setPicUrl(String picUrl) {
		this.picUrl = picUrl;
	}
	public final String getStartTime() {
		return startTime;
	}
	public final void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public final String getOrderId() {
		return orderId;
	}
	public final void setOrderId(String orderId) {
		this.orderId = orderId;
	}
	public final String getTypeId() {
		return typeId;
	}
	public final void setTypeId(String typeId) {
		this.typeId = typeId;
	}
	

}
